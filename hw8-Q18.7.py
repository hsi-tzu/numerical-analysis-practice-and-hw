def f(x,x0,x1): #定義方程式,才不用一直寫一些很複雜的東西
  return (x-x0)/(x1-x0)

#將對應數字帶入課本公式18.22

f1=f(4,5,3)*19+f(4,3,5)*99  #第一內插(x0=3,x1=5)
f2=f(4,5,3)*f(4,2,3)*19+f(4,3,5)*f(4,2,5)*99+f(4,5,2)*f(4,3,2)*6 #第二次內插(x0=2,x1=3,x2=5)
f3=f(4,5,3)*f(4,2,3)*f(4,7,3)*19+f(4,3,5)*f(4,2,5)*f(4,7,5)*99+f(4,5,2)*f(4,3,2)*f(4,7,2)*6+f(4,2,7)*f(4,3,7)*f(4,5,7)*291 #第三次內插(x0=2,x1=3,x2=5,x3=7)
print(f1,f2,f3)